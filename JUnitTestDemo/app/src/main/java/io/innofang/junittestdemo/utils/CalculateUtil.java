public class CalculateUtil {

    public static int add(int a, int b) {
        return a + b;
    }

    public static int div(int a, int b) {
        return a - b;
    }

    public static int mul(int a, int b) {
        return a * b;
    }

    public static double sub(int a, int b) {
        return a / b * 1.0;
    }

}
